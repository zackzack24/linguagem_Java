public class Salarios {
      public static void main(String[] args) {
            int salario = 5000;
            float porcentagemSalario30 = salario * (30 / 100);
            float porcentagemSalario15 = salario * (15 / 100);
            float porcentagemSalario5 = salario * (5 / 100);
            System.out.println("O seu salário é " + salario );
            System.out.println("30% equivale a " + porcentagemSalario30);
            System.out.println("15% equivale a " + porcentagemSalario15);
            System.out.println("5% equivale a " + porcentagemSalario5);
      }

}
