public class Aula07Arrays01 {

    public static void main(String[] args) {
        int idadeA = 10;
        int idadeB = 15;
        int idadeC = 20;

        int [] idadesABC = new int[3];
        idadesABC[0] = 10;
        idadesABC[1] = 15;
        idadesABC[2] = 20;

        System.out.println(idadeA);
        System.out.println(idadeB);
        System.out.println(idadeC);

        System.out.println(idadesABC[0]);
        System.out.println(idadesABC[1]);
        System.out.println(idadesABC[2]);
    }
}
