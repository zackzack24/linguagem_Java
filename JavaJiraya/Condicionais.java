public class Condicionais {
      public static void main(String[] args) {
            boolean condicao = true;
            if (condicao = false) {
                  System.out.println("Funcionando");
            } else {
                  System.out.println("Você é desfuncional");
            }
            // Fora do If
            System.out.println(condicao);
      }
   
}
